package br.edu.up.models;
public class Pessoas11 {
    
    private String nome;
    private String sexo;

    public Pessoas11(String nome, String sexo){
        this.nome = nome;
        this.sexo = sexo;
    }

    public String getNome(){
        return nome;
    }

    public String getSexo(){
        return sexo;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    

}
